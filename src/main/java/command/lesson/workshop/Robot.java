package command.lesson.workshop;

public class Robot {

    public void turnOn() {
        System.out.println("The robot has been turned on!");
    }

    public void cut() {
        System.out.println("The robot cuts!");
    }

    public void drill() {
        System.out.println("The robot drills!");
    }

    public void turnOff() {
        System.out.println("The robot has been turned off!");
    }
}
